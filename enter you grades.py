marks = int(input("What is your grade? Enter your marks: "))

if marks >= 90:
    print("Grade is A")
elif marks >= 80:
    print("Grade is B")
elif marks >= 70:
    print("Grade is C")
elif marks >= 60:
    print("Grade is D")
elif marks >= 50:
    print("Grade is E")
elif marks >= 40:
    print("Failed")